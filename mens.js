var McartArr = JSON.parse(localStorage.getItem("McartData")) || [];
function addToCart(i) {
  var product = mensData.filter(function (elem, index) {
    return index == i;
  });
  McartArr.push(product[0]);
  localStorage.setItem("McartData", JSON.stringify(McartArr));
}
